    String fromPlace = fromText.getText();
    String toPlace = toText.getText();
    String PNRNumber = pnrNumberText.getText();
    
    Date selectedDate = journeyDateText.getDate();
    
    if (selectedDate == null) {
        JOptionPane.showMessageDialog(null, "Please select a journey date");
        return;
    }
    
    SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
    String journeyDate = dateFormat.format(selectedDate);
    
       try {
    insertReservation(trainNumber, trainName, classType, fromPlace, toPlace, journeyDate,PNRNumber);
} catch (ClassNotFoundException ex) {
    JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
}


            
