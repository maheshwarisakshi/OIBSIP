    private static void insertReservation(String trainNumber, String trainName, String classType, String fromPlace, String toPlace, String journeyDate , String PNRNumber) throws ClassNotFoundException {
        try (
             Connection conn = DBConnection.getConnection();
             PreparedStatement stmt = conn.prepareStatement("INSERT INTO Reservations (trainNumber, trainName, classType, fromPlace, toPlace, journeyDate , PNRNumber) VALUES (?, ?, ?, ?, ?, ?,?)")) {
            stmt.setString(1, trainNumber);
            stmt.setString(2, trainName);
            stmt.setString(3, classType);
            stmt.setString(4, fromPlace);
            stmt.setString(5, toPlace);
            stmt.setString(6, journeyDate);
            stmt.setString(7, PNRNumber);
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Reservation Successful");
        } catch (SQLException ex) {
             System.out.println("Database error occurred: " + ex.getMessage());
        }
    }
